answer1 = input("You have come to crossroad. Do you go left or right?")

if answer1.lower() == "left":
    print("Game Over.")
elif answer1.lower() == "right":
    answer2 = input("You are in a large room with a golden door. Do you open the door? Yes or No.")
    if answer2.lower() == "yes":
        print("Hah! That was a trick! The door falls on you crushing you. Game Over.")
    elif answer2.lower() == "no":
        answer3 = input("You are now in front of a large lake. A giant talking bear appears and asks you to choose between three shells. Do you choose the first, the second, or the third?")
        if answer3.lower() == "first":
            print("The bear doesn't like that answer and eats you. Game over.")
        elif answer3.lower() == "third":
            print("That is not the right answer. Game Over!")
        elif answer3.lower() == "second":
            print("This pleases the bear. He gives you a treasure chest and flies off into the sunset. You win!")
